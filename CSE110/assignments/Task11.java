import java.util.Scanner;
public class Task11{
  public static void main(String []args){
    Scanner goat = new Scanner(System.in);
    System.out.println("Give a number");
    int n = goat.nextInt();
    int v;
    n = v;
    for(int c=1; c<=n; c++){
      for(int j=1; j<=(n-c); j++){
        System.out.println(" ");
      }
      for(int x=n; x<=n; x++){
        System.out.print(x);
        
      }
      System.out.println();
      v--;
    }
  }
}
